from aiogram import Router, F
from aiogram.types import CallbackQuery
from keyboards import email_menu

router = Router()

@router.callback_query(F.data == "email_partnership")
async def email_partnership(callback: CallbackQuery):
    # Отправка всех шаблонов в одном сообщении
    text = "\n\n".join([
        "    Добрый день, [Имя],\n"
        "Мы изучили вашу компанию и видим возможности для продуктивного сотрудничества. "
        "Готовы обсудить детали взаимодействия, определить возможные точки соприкосновения "
        "и предложить оптимальный формат партнёрства. "
        "Если вам это интересно, давайте согласуем удобное время для встречи.\n\n"
        "Ждём вашего ответа!",

        "    Уважаемый [Имя],\n"
        "Мы готовы предложить вам выгодные условия сотрудничества, которые помогут "
        "развивать ваш бизнес и достичь новых целей. "
        "Хотелось бы обсудить возможные направления взаимодействия, включая [перечислить варианты]. "
        "Если у вас есть интерес, давайте согласуем формат общения.\n\n"
        "С нетерпением жду вашего ответа!",

        "    Здравствуйте, [Имя],\n"
        "Хотим представить вам наши услуги и обсудить возможности для продуктивного сотрудничества. "
        "Мы уверены, что наше партнёрство может принести значительную выгоду обеим сторонам. "
        "Давайте запланируем встречу или звонок, чтобы обсудить детали.\n\n"
        "Будем рады вашему ответу!"
    ])
    
    # Отправляем все шаблоны одним сообщением с кнопкой "Назад"
    await callback.message.answer(
        text,
        reply_markup=email_menu  # Клавиатура с кнопкой назад
    )
    await callback.answer()
