from aiogram import Router, F
from aiogram.types import CallbackQuery
from keyboards import email_menu

router = Router()

# Обработчик кнопки "Запрос информации"
@router.callback_query(F.data == "email_info")
async def email_info(callback: CallbackQuery):
    # Отправка всех шаблонов в одном сообщении
    text = "\n\n".join([
        "    Здравствуйте, [Имя],\n"
        "Буду признателен, если вы сможете предоставить мне подробную информацию по [указать тему запроса]. "
        "Мне нужны сведения о [конкретные детали], включая сроки, условия и возможные варианты. "
        "Если у вас есть дополнительные материалы или ссылки, буду рад их получить.\n\n"
        "Заранее благодарю за ваше время и помощь!",

        "    Уважаемый [Имя],\n"
        "Хотел бы уточнить у вас детали по [конкретный вопрос]. "
        "Меня интересует [описание интересующей информации] и возможные дополнительные условия. "
        "Буду признателен, если вы сможете предоставить актуальные данные или направить меня к специалисту, который сможет помочь.\n\n"
        "Спасибо за уделенное время!",

        "    Уважаемый [Имя],\n"
        "Прошу предоставить расценки на [указать продукт/услугу]. "
        "Хотелось бы узнать подробности о ценах, условиях оплаты, скидках и сроках поставки. "
        "Если возможно, отправьте, пожалуйста, прайс-лист или коммерческое предложение.\n\n"
        "Буду признателен за быстрый ответ!"
    ])
    
    # Отправляем все шаблоны одним сообщением с кнопкой "Назад"
    await callback.message.answer(
        text,
        reply_markup=email_menu  # Клавиатура с кнопкой назад
    )
    await callback.answer()
